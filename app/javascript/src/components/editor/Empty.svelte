  <h1 class="mb-0">Welcome to the Workshop.codes Overwatch Workshop Script Editor</h1>
  <small class="text-dark">Catchy name, right?</small>

  <div class="line-height">
    <p>With this editor you can create and edit your Workshop modes using a text based editor. If you have not yet used the Workshop using this editor as a starting point might be difficult. If you have never used a text based editor before but you have used the Workshop, consider this the second step to becoming a programmer!</p>

    <p>
      Features:
      <ul>
        <li>Autocomplete any Workshop actions and values.</li>
        <li>Automatic variable detecting, no need to declare your variables anywhere.</li>
        <li>Relevant syntax highlighting.</li>
        <li>Folders, wow!</li>
        <li>Integrated Wiki search.</li>
      </ul>
    <p>

    <p>Create a new project to get started. Import your existing script or start from zero.</p>

    <p class="text-white">
      <mark>This editor is currently in an early alpha version. Expect bugs, many of them!</mark>
    </p>

    <p>
      The editor is missing many features that would make it much more useful. Here is a list is planned features:
      <ul>
        <li>Better autocomplete. Currently the autocomplete does not auto fill parameters.</li>
        <li>Automatic wiki searching on auto complete.</li>
        <li>Better syntax highlighting.</li>
        <li>OverPy! Writing scripts using a HLL is much more enjoyable, getting OverPy in is a high priority.</li>
        <li>Automatic error detection.</li>
        <li>Optional semicolons.</li>
        <li>Search and replace across all files.</li>
        <li>More customisation; colors, fonts, sizes, etc.</li>
      </ul>
    <p>

    <p>
      Known bugs:
      <ul>
        <li>Spaces break autocomplete results.</li>
        <li>Variables that are only stated in a For Global (or Player) Variable Loop will not be properly compiled.</li>
        <li>Syntax highlighting can be very dumb in places.</li>
        <li>Rules without names might not be properly imported.</li>
        <li>You can't rename projects.</li>
      </ul>
    <p>
